                "    efficiency = df.copy()\n",
                "    \n",
                "    # Encoding efficiency\n",
                "    if 'encoding_inference_time_seconds' in df.columns:\n",
                "        efficiency['encoding_throughput_per_sec'] = (\n",
                "            df['embeddings_count'] / df['encoding_inference_time_seconds']\n",
                "        ).round(2)\n",
                "        efficiency['avg_encoding_time_ms'] = (\n",
                "            (df['encoding_inference_time_seconds'] * 1000) / df['embeddings_count']\n",
                "        ).round(2)\n",
                "    \n",
                "    # Indexing efficiency\n",
                "    if 'indexing_time_seconds' in df.columns:\n",
                "        efficiency['indexing_throughput_per_sec'] = (\n",
                "            df['embeddings_count'] / df['indexing_time_seconds']\n",
                "        ).round(2)\n",
                "        efficiency['avg_indexing_time_ms'] = (\n",
                "            (df['indexing_time_seconds'] * 1000) / df['embeddings_count']\n",
                "        ).round(2)\n",
                "    \n",
                "    # Total processing time\n",
                "    if 'encoding_inference_time_seconds' in df.columns and 'indexing_time_seconds' in df.columns:\n",
                "        efficiency['total_time_seconds'] = (\n",
                "            df['encoding_inference_time_seconds'] + df['indexing_time_seconds']\n",
                "        ).round(2)\n",
                "        efficiency['overall_throughput_per_sec'] = (\n",
                "            df['embeddings_count'] / efficiency['total_time_seconds']\n",
                "        ).round(2)\n",
                "        efficiency['avg_total_time_ms'] = (\n",
                "            (efficiency['total_time_seconds'] * 1000) / df['embeddings_count']\n",
                "        ).round(2)\n",
                "    \n",
                "    # Storage efficiency\n",
                "    if 'embeddings_size_mb' in df.columns:\n",
                "        efficiency['storage_per_1k_embeddings_mb'] = (\n",
                "            (df['embeddings_size_mb'] / df['embeddings_count']) * 1000\n",
                "        ).round(4)\n",
                "        efficiency['storage_per_embedding_kb'] = (\n",
                "            (df['embeddings_size_mb'] * 1024) / df['embeddings_count']\n",
                "        ).round(4)\n",
                "    \n",
                "    # Dimension efficiency\n",
                "    if 'embedding_dimension' in df.columns:\n",
                "        efficiency['bytes_per_dimension'] = (\n",
                "            (df['embeddings_size_mb'] * 1024 * 1024) / (df['embeddings_count'] * df['embedding_dimension'])\n",
                "        ).round(4)\n",
                "    \n",
                "    return efficiency\n",
