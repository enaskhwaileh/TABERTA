                "def calculate_efficiency_metrics(df):\n",
                "    '''Calculate comprehensive efficiency metrics for reviewer comments'''\n",
                "    if len(df) == 0:\n",
                "        return pd.DataFrame()\n",
                "    \n",
                "    # Ensure we have needed columns\n",
