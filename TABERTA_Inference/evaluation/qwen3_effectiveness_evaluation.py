#!/usr/bin/env python3
"""
Evaluate Qwen3-Embedding retrieval effectiveness on TABERTA paper datasets.

This script reuses the existing Qdrant collections generated by
embeddings/generate_all_embeddings_with_metrics.py and computes the
dataset-specific metrics needed for the paper tables:

- TabFact: Precision@5, Recall@5, F1@5
- FeTaQA / OTTQA: R@1, R@5, R@10, MRR@10, NDCG@10
- Spider: CR@1, CR@5, CR@10, MRR@10, NDCG@10

WikiDBs-10k currently has embeddings/efficiency metrics in this repo, but no
query/qrels file, so it is intentionally not reported as effectiveness.
"""

from __future__ import annotations

import argparse
import csv
import io
import json
import math
import tarfile
import time
from pathlib import Path
from typing import Iterable

import numpy as np
import pandas as pd
import torch
from qdrant_client import QdrantClient
from sentence_transformers import SentenceTransformer
from tqdm import tqdm


REPO_ROOT = Path(__file__).resolve().parents[1]
DATASETS_DIR = REPO_ROOT / "datasets"
OUTPUT_DIR = REPO_ROOT / "evaluation" / "results"
MODEL_NAME = "Qwen/Qwen3-Embedding-8B"
QDRANT_HOST = "localhost"
QDRANT_PORT = 6333


def read_parquet_from_tar(tar_path: Path, member_name: str) -> pd.DataFrame:
    with tarfile.open(tar_path, "r:gz") as tar:
        extracted = tar.extractfile(member_name)
        if extracted is None:
            raise FileNotFoundError(f"{member_name} not found in {tar_path}")
        return pd.read_parquet(io.BytesIO(extracted.read()))


def read_jsonl_from_tar(tar_path: Path, member_name: str) -> list[dict]:
    with tarfile.open(tar_path, "r:gz") as tar:
        extracted = tar.extractfile(member_name)
        if extracted is None:
            raise FileNotFoundError(f"{member_name} not found in {tar_path}")
        return [json.loads(line) for line in extracted.read().decode("utf-8").splitlines() if line]


def load_model() -> SentenceTransformer:
    model_kwargs = {"dtype": torch.float16} if torch.cuda.is_available() else {}
    model = SentenceTransformer(MODEL_NAME, model_kwargs=model_kwargs)
    if torch.cuda.is_available():
        model = model.to("cuda")
    return model


def encode_queries(model: SentenceTransformer, queries: list[str], batch_size: int) -> np.ndarray:
    return model.encode(
        queries,
        batch_size=batch_size,
        show_progress_bar=True,
        convert_to_numpy=True,
        normalize_embeddings=True,
    )


def search_collection(
    qdrant: QdrantClient,
    collection_name: str,
    query_vectors: np.ndarray,
    limit: int,
) -> list[list[str]]:
    retrieved: list[list[str]] = []
    for vector in tqdm(query_vectors, desc=f"Searching {collection_name}"):
        hits = qdrant.search(
            collection_name=collection_name,
            query_vector=vector.tolist(),
            limit=limit,
            with_payload=True,
        )
        retrieved.append([str(hit.payload.get("table_name")) for hit in hits])
    return retrieved


def recall_at_k(retrieved: list[list[str]], relevant: list[set[str]], k: int) -> float:
    values = []
    for ranking, gold in zip(retrieved, relevant):
        values.append(len(set(ranking[:k]) & gold) / len(gold) if gold else 0.0)
    return float(np.mean(values)) if values else 0.0


def precision_recall_f1_at_k(retrieved: list[list[str]], relevant: list[set[str]], k: int) -> dict[str, float]:
    precision_values = []
    recall_values = []
    f1_values = []
    for ranking, gold in zip(retrieved, relevant):
        top_k = set(ranking[:k])
        true_positive = len(top_k & gold)
        precision = true_positive / k
        recall = true_positive / len(gold) if gold else 0.0
        f1 = (2 * precision * recall / (precision + recall)) if precision + recall else 0.0
        precision_values.append(precision)
        recall_values.append(recall)
        f1_values.append(f1)
    return {
        "Precision@5": float(np.mean(precision_values)) if precision_values else 0.0,
        "Recall@5": float(np.mean(recall_values)) if recall_values else 0.0,
        "F1@5": float(np.mean(f1_values)) if f1_values else 0.0,
    }


def mrr_at_k(retrieved: list[list[str]], relevant: list[set[str]], k: int) -> float:
    values = []
    for ranking, gold in zip(retrieved, relevant):
        rr = 0.0
        for rank, table_id in enumerate(ranking[:k], 1):
            if table_id in gold:
                rr = 1.0 / rank
                break
        values.append(rr)
    return float(np.mean(values)) if values else 0.0


def ndcg_at_k(retrieved: list[list[str]], relevant: list[set[str]], k: int) -> float:
    values = []
    for ranking, gold in zip(retrieved, relevant):
        dcg = 0.0
        for idx, table_id in enumerate(ranking[:k]):
            if table_id in gold:
                dcg += 1.0 / math.log2(idx + 2)
        ideal_hits = min(k, len(gold))
        idcg = sum(1.0 / math.log2(idx + 2) for idx in range(ideal_hits))
        values.append(dcg / idcg if idcg else 0.0)
    return float(np.mean(values)) if values else 0.0


def containment_recall_at_k(retrieved: list[list[str]], gold_tables: list[set[str]], k: int) -> float:
    values = []
    for ranking, gold in zip(retrieved, gold_tables):
        values.append(1.0 if gold.issubset(set(ranking[:k])) else 0.0)
    return float(np.mean(values)) if values else 0.0


def evaluate_simple_retrieval(
    model: SentenceTransformer,
    qdrant: QdrantClient,
    dataset: str,
    queries: list[str],
    relevant: list[set[str]],
    batch_size: int,
    limit: int = 10,
) -> tuple[list[list[str]], dict[str, float]]:
    start = time.time()
    query_vectors = encode_queries(model, queries, batch_size=batch_size)
    encode_seconds = time.time() - start
    search_start = time.time()
    retrieved = search_collection(qdrant, f"{dataset}_qwen3_8b", query_vectors, limit=limit)
    search_seconds = time.time() - search_start
    metrics = {
        "queries": len(queries),
        "query_encoding_seconds": encode_seconds,
        "search_seconds": search_seconds,
        "avg_search_ms": (search_seconds * 1000 / len(queries)) if queries else 0.0,
        "R@1": recall_at_k(retrieved, relevant, 1),
        "R@5": recall_at_k(retrieved, relevant, 5),
        "R@10": recall_at_k(retrieved, relevant, 10),
        "MRR@10": mrr_at_k(retrieved, relevant, 10),
        "NDCG@10": ndcg_at_k(retrieved, relevant, 10),
    }
    return retrieved, metrics


def tabfact_inputs() -> tuple[list[str], list[set[str]]]:
    records = read_jsonl_from_tar(
        DATASETS_DIR / "tabfact.tar.gz",
        "tabfact/tabfact_validation_labeled.json",
    )
    return [row["statement"] for row in records], [{row["table_id"]} for row in records]


def fetaqa_inputs(split: str = "validation") -> tuple[list[str], list[set[str]]]:
    df = read_parquet_from_tar(
        DATASETS_DIR / "fetaqa.tar.gz",
        f"fetaqa/queries/{split}.parquet",
    )
    return df["query"].astype(str).tolist(), [{str(table_id)} for table_id in df["table_id"]]


def ottqa_inputs() -> tuple[list[str], list[set[str]]]:
    df = read_parquet_from_tar(
        DATASETS_DIR / "ottqa.tar.gz",
        "ottqa/queries/validation.parquet",
    )
    return df["query"].astype(str).tolist(), [{str(table_id)} for table_id in df["table_id"]]


def spider_inputs(split: str = "validation") -> tuple[list[str], list[set[str]]]:
    df = pd.read_parquet(REPO_ROOT / "datasets" / "spider" / "queries" / f"{split}_enriched.parquet")
    queries = df["question"].astype(str).tolist()
    relevant = [
        {f"{row.db_id}::{table_name}" for table_name in row.gold_tables}
        for row in df.itertuples(index=False)
    ]
    return queries, relevant


def write_rankings(path: Path, dataset: str, queries: list[str], relevant: list[set[str]], retrieved: list[list[str]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["dataset", "query_index", "query", "relevant", "rank", "retrieved_table"])
        writer.writeheader()
        for idx, (query, gold, ranking) in enumerate(zip(queries, relevant, retrieved)):
            for rank, table_id in enumerate(ranking, 1):
                writer.writerow(
                    {
                        "dataset": dataset,
                        "query_index": idx,
                        "query": query,
                        "relevant": "|".join(sorted(gold)),
                        "rank": rank,
                        "retrieved_table": table_id,
                    }
                )


def run(args: argparse.Namespace) -> None:
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    model = load_model()
    qdrant = QdrantClient(host=args.qdrant_host, port=args.qdrant_port, timeout=120)

    loaders = {
        "tabfact": tabfact_inputs,
        "fetaqa": fetaqa_inputs,
        "ottqa": ottqa_inputs,
        "spider": spider_inputs,
    }

    summary_rows = []
    for dataset in args.datasets:
        print(f"\n=== Evaluating {dataset} / qwen3_8b ===")
        queries, relevant = loaders[dataset]()
        if args.limit:
            queries = queries[: args.limit]
            relevant = relevant[: args.limit]

        retrieved, metrics = evaluate_simple_retrieval(
            model=model,
            qdrant=qdrant,
            dataset=dataset,
            queries=queries,
            relevant=relevant,
            batch_size=args.batch_size,
            limit=10,
        )

        if dataset == "tabfact":
            metrics.update(precision_recall_f1_at_k(retrieved, relevant, 5))
        if dataset == "spider":
            metrics.update(
                {
                    "CR@1": containment_recall_at_k(retrieved, relevant, 1),
                    "CR@5": containment_recall_at_k(retrieved, relevant, 5),
                    "CR@10": containment_recall_at_k(retrieved, relevant, 10),
                }
            )

        row = {"dataset": dataset, "model": "qwen3_8b", **metrics}
        summary_rows.append(row)
        write_rankings(OUTPUT_DIR / f"{dataset}_qwen3_8b_rankings.csv", dataset, queries, relevant, retrieved)

    summary = pd.DataFrame(summary_rows)
    summary_path = OUTPUT_DIR / "qwen3_effectiveness_summary.csv"
    summary.to_csv(summary_path, index=False)
    print(f"\nSaved {summary_path}")
    print(summary.to_string(index=False))


def parse_args(argv: Iterable[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--datasets",
        nargs="+",
        choices=["tabfact", "fetaqa", "ottqa", "spider"],
        default=["tabfact", "fetaqa", "ottqa", "spider"],
    )
    parser.add_argument("--batch-size", type=int, default=8)
    parser.add_argument("--limit", type=int, default=None, help="Debug limit for each dataset")
    parser.add_argument("--qdrant-host", default=QDRANT_HOST)
    parser.add_argument("--qdrant-port", type=int, default=QDRANT_PORT)
    return parser.parse_args(argv)


if __name__ == "__main__":
    run(parse_args())
